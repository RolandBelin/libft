#include "includes/libft.h"

INT8		*ft_strcpy(INT8 *dst, const INT8 *src)
{
	size_t	i;

	i = 0;
	while (src[i])
	{
		dst[i] = src[i];
		++i;
	}
	dst[i] = '\0';
	return (dst);
}
