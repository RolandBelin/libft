#include "includes/libft.h"

INT32		ft_isxdigit(INT32 c)
{
	if ((c >= 'A' && c <= 'F') || (c >= 'a' && c <= 'f') || ft_isdigit(c))
		return (4096);
	else
		return (0);
}
