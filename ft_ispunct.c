#include "includes/libft.h"

INT32		ft_ispunct(INT32 c)
{
	if (c == '!' || c == '"' || c == '#' || c == '%' || c == '&' || c == '\'')
		return (4);
	if (c == '(' || c == ')' || c == ';' || c == '<' || c == '=' || c == '>')
		return (4);
	if (c == '?' || c == '[' || c == '\\' || c == ']' || c == '*' || c == '+')
		return (4);
	if (c == ',' || c == '-' || c == '.' || c == '/' || c == ':' || c == '^')
		return (4);
	if (c == '_' || c == '{' || c == '|' || c == '}' || c == '~' || c == '$')
		return (4);
	if (c == '@' || c == '`' || c == 272 || c == 274 || c == 280 || c == 296)
		return (4);
	return (0);
}
